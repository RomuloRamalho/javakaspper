package ex1ao10;

public class Ex2 {
	
//	Dado o tamanho do lado de um quadrado, 
//	calcular a área e o perímetro do mesmo.

	public static void main(String[] args) {
		
	
		double base = 4;
		double altura = 4;
		double areaQua = base * altura;
		double periQua = (base + altura) * 2;
		
		System.out.println("A área do quadrado é: " + areaQua);
		System.out.println("O perímetro do quadrado é: " + periQua);
		
		
	}
	
	
}
