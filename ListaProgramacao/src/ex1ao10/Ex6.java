package ex1ao10;

public class Ex6 {

//	Ler dois números inteiros e exibir o quociente
//	e o resto da divisão inteira entre eles.

	public static void main(String[] args) {

		
		int num1 = 15;
		int num2 = 6;
		int res = num1 / num2;
		int resto = num1 % num2;
		
		
		System.out.println(num1 + " dividido por " + num2 + " = " + res);
		System.out.println("O resto da divisão é: " + resto);
		
		
	}
}
