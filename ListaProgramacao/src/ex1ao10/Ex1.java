package ex1ao10;

public class Ex1 {
	
//	Dado o tamanho da base e da altura de um retângulo, calcular a sua área
//	e o seu perímetro;
	
	public static void main(String[] args) {
		
		double base, altura, areaRet, periRet;
		
		base = 5;
		altura = 10;
		areaRet = base * altura;
		periRet = 5 + 5 + 10 + 10;
		
		System.out.println("A área do retângulo é: " + areaRet);
		System.out.println("O perímetro do retângulo é: " + periRet);
		
	}

}
